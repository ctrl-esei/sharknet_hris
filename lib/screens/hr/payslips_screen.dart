String _normalizeStatus(dynamic value) {
  final String raw =
      value?.toString().trim().toLowerCase() ??
          'draft';

  final String normalized = raw
      .replaceAll('-', '_')
      .replaceAll(' ', '_');

  if (normalized == 'pendingapproval' ||
      normalized == 'pending') {
    return 'pending_approval';
  }

  return normalized;
}

String _actorLabel(dynamic value) {
  if (value is Map<String, dynamic>) {
    final String fullName =
        value['fullName']?.toString().trim() ??
            '';

    final String email =
        value['email']?.toString().trim() ??
            '';

    final String role =
        value['role']?.toString().trim() ??
            '';

    if (fullName.isNotEmpty &&
        role.isNotEmpty) {
      return '$fullName (${_formatLabel(role)})';
    }

    if (fullName.isNotEmpty) {
      return fullName;
    }

    if (email.isNotEmpty) {
      return email;
    }

    final String uid =
        value['uid']?.toString().trim() ??
            '';

    if (uid.isNotEmpty) {
      return uid;
    }
  }

  if (value is Map) {
    return _actorLabel(
      value.map<String, dynamic>(
        (
          dynamic key,
          dynamic item,
        ) =>
            MapEntry<String, dynamic>(
          key.toString(),
          item,
        ),
      ),
    );
  }

  final String text =
      value?.toString().trim() ?? '';

  return text.isEmpty ? 'Not available' : text;
}

double _readNumber(dynamic value) {
  if (value is num) {
    return value.toDouble();
  }

  return double.tryParse(
        value?.toString() ?? '',
      ) ??
      0;
}

String _referenceId(dynamic value) {
  if (value is DocumentReference) {
    return value.id;
  }

  final String raw =
      value?.toString() ?? 'employee';

  return raw.contains('/')
      ? raw.split('/').last
      : raw;
}

DateTime? _dateFromValue(dynamic value) {
  if (value is Timestamp) {
    return value.toDate();
  }

  if (value is DateTime) {
    return value;
  }

  if (value is String) {
    return DateTime.tryParse(value);
  }

  return null;
}

String _formatDate(DateTime? date) {
  if (date == null) {
    return 'Not available';
  }

  const List<String> months =
      <String>[
    'Jan',
    'Feb',
    'Mar',
    'Apr',
    'May',
    'Jun',
    'Jul',
    'Aug',
    'Sep',
    'Oct',
    'Nov',
    'Dec',
  ];

  return '${months[date.month - 1]} '
      '${date.day}, ${date.year}';
}

String _formatLabel(String value) {
  if (value.trim().isEmpty) {
    return 'Unknown';
  }

  return value
      .replaceAll('_', ' ')
      .split(' ')
      .where(
        (String word) => word.isNotEmpty,
      )
      .map(
        (String word) =>
            '${word[0].toUpperCase()}'
            '${word.substring(1).toLowerCase()}',
      )
      .join(' ');
}

String _initials(String fullName) {
  final List<String> parts = fullName
      .trim()
      .split(RegExp(r'\s+'))
      .where(
        (String part) => part.isNotEmpty,
      )
      .toList();

  if (parts.isEmpty) {
    return '?';
  }

  if (parts.length == 1) {
    return parts.first[0].toUpperCase();
  }

  return '${parts.first[0]}${parts.last[0]}'
      .toUpperCase();
}
